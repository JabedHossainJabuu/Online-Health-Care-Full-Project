    <ol>
        <li>
            <p><strong>Maintain a Balanced Diet:</strong></p>
            <p>A well-balanced diet forms the foundation of good health. It is vital to consume a variety of nutrient-rich foods, including fruits, vegetables, whole grains, lean proteins, and healthy fats. This ensures that our bodies receive essential vitamins, minerals, and antioxidants necessary for optimal functioning. Additionally, drinking an adequate amount of water daily helps keep our bodies hydrated and aids in digestion.</p>
        </li>

        <li>
            <p><strong>Regular Physical Activity:</strong></p>
            <p>Engaging in regular physical activity is vital for maintaining good health. It not only helps in weight management but also boosts cardiovascular health, strengthens muscles and bones, and improves mental well-being. Aim for at least 30 minutes of moderate-intensity exercise most days of the week, such as brisk walking, cycling, or swimming.</p>
        </li>

        <li>
            <p><strong>Sufficient Rest and Sleep:</strong></p>
            <p>Adequate rest and quality sleep are crucial for our bodies to rejuvenate and repair. Aim for 7-9 hours of sleep per night to support optimal cognitive function, immune system strength, and overall well-being. Establish a relaxing bedtime routine and create a conducive sleep environment to ensure quality sleep.</p>
        </li>

        <li>
            <p><strong>Practice Good Hygiene:</strong></p>
            <p>Maintaining good hygiene is essential for preventing the spread of infections and diseases. This includes regular handwashing with soap and water, especially before meals and after using the restroom. Covering the mouth and nose while coughing or sneezing, using tissues or the elbow, can help minimize the transmission of respiratory illnesses. Additionally, maintaining cleanliness in our living spaces and personal belongings contributes to a healthy environment.</p>
        </li>

        <li>
            <p><strong>Immunizations and Regular Health Check-ups:</strong></p>
            <p>Keeping up with immunizations and regular health check-ups is crucial for preventive care. Vaccinations protect against various infectious diseases and help build immunity. Routine check-ups allow for early detection and management of potential health issues, promoting overall well-being.</p>
        </li>

